<template>
    <JdModal
        modal="mProduccionTrazabilidad"
        :buttons="buttons"
        @button-click="(action) => this[action]()"
    >
        <div class="container-datos">
            <p style="grid-column: 1/3">
                <strong>--- Orden de producción ---</strong>
            </p>

            <JdInput
                type="date"
                label="Fecha"
                :nec="true"
                v-model="modal.produccion_orden.fecha"
                :disabled="modal.mode == 3"
                style="grid-column: 1/3"
            />

            <template v-if="modal.produccion_orden.tipo != 2">
                <JdSelect
                    label="Máquina"
                    :nec="true"
                    v-model="modal.produccion_orden.maquina"
                    :lista="
                        modal.maquinas?.filter(
                            (a) => a.produccion_tipo == modal.produccion_orden.tipo,
                        ) || []
                    "
                    :disabled="modal.mode == 3"
                    style="grid-column: 3/5"
                />
            </template>

            <JdSelectQuery
                label="Producto"
                :nec="true"
                v-model="modal.produccion_orden.articulo"
                :lista="modal.articulos"
                :disabled="modal.mode == 3"
                style="grid-column: 1/4"
            />

            <JdInput
                type="number"
                label="Cantidad planificada"
                :nec="true"
                v-model="modal.produccion_orden.cantidad"
                :disabled="modal.mode == 3"
                style="grid-column: 1/3"
            />
        </div>

        <div class="mrg-btm2">
            <p class="mrg-btm05">
                <strong>--- Insumos ---</strong>
            </p>
            <JdTable
                :columns="columns1"
                :datos="modal.produccion_orden.insumos || []"
                :seeker="false"
                :download="false"
            >
                <!-- <template v-slot:cAction="{ item }">
                    <JdButton tipo="2" :small="true" icon="fa-regular fa-folder-open" :disabled="modal.mode == 3"
                        @click="verCompra(item)" />
                </template> -->
            </JdTable>
        </div>

        <div>
            <p class="mrg-btm05">
                <strong>--- Productos terminados ---</strong>
            </p>
            <JdTable
                :columns="columns2"
                :datos="modal.produccion_orden.productos_terminados || []"
                :seeker="false"
                :download="false"
            >
            </JdTable>
        </div>
    </JdModal>
</template>

<script>
import JdModal from '@/components/JdModal.vue'
import JdInput from '@/components/inputs/JdInput.vue'
import JdSelectQuery from '@/components/inputs/JdSelectQuery.vue'
import JdSelect from '@/components/inputs/JdSelect.vue'
import JdTable from '@/components/JdTable.vue'
// import JdButton from '@/components/inputs/JdButton.vue'

import { useAuth } from '@/pinia/auth'
import { useModals } from '@/pinia/modals'

import { urls, get } from '@/utils/crud'

export default {
    components: {
        JdModal,
        JdInput,
        JdSelectQuery,
        JdSelect,
        JdTable,
        // JdButton,
    },
    data: () => ({
        useAuth: useAuth(),
        useModals: useModals(),

        modal: {},

        buttons: [{ text: 'Imprimir', action: 'imprimir', show: true }],

        columns1: [
            {
                id: 'articulo',
                title: 'Nombre',
                prop: 'articulo1.nombre',
                width: '20rem',
                show: true,
            },
            {
                id: 'lote',
                prop: 'lote_padre1.lote',
                title: 'Lote',
                width: '8rem',
                show: true,
            },
            {
                id: 'fv',
                title: 'Fecha vencimiento',
                prop: 'lote_padre1.fv',
                width: '8rem',
                show: true,
            },
            {
                id: 'cantidad',
                title: 'Cantidad',
                toRight: true,
                width: '8rem',
                show: true,
            },
        ],

        columns2: [
            {
                id: 'lote',
                title: 'Lote',
                width: '10rem',
                show: true,
            },
            {
                id: 'fv',
                title: 'Fecha vencimiento',
                width: '8rem',
                show: true,
            },
            {
                id: 'cantidad',
                title: 'Cantidad',
                toRight: true,
                width: '10rem',
                show: true,
            },
        ],
    }),
    created() {
        this.modal = this.useModals.mProduccionTrazabilidad
    },
    methods: {
        async verCompra(item) {
            this.useAuth.setLoading(true, 'Cargando compra...')
            const res = await get(urls.transacciones + '/uno/' + item.transaccion)
            this.useAuth.setLoading(false)

            if (res.code != 0) return

            const send = {
                transaccion: res.data,
                socio: { ...res.data.socio1 },
                socios: [{ ...res.data.socio1 }],
                monedas: [{ ...res.data.moneda1 }],
                pedidos: res.data.socio_pedido ? [{ ...res.data.socio_pedido1 }] : [],
            }

            this.useModals.setModal('mTransaccion', 'Ver compra', 3, send, true)
        },
        imprimir() {
            alert('impreso')
        },
    },
}
</script>

